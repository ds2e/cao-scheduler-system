export default function WaiterNavbar({ currentActiveTab, tabs, requestSwitchTab }) {
    const navigationTabs = [...tabs]
        .map(item => ({
            ...item,
            current: currentActiveTab == item.name,
        }));

    return (
        <div className="fixed bottom-2 left-2 flex gap-x-2">
            {navigationTabs.map((tab, tab_ind) => {
                return (
                    <button
                        key={tab.name + tab_ind}
                        aria-current={tab.current ? 'page' : undefined}
                        onClick={() => requestSwitchTab(tab.name)}
                        className={`${tab.current ? "bg-theme-secondary hover:bg-theme-secondary-highlight" : "bg-white hover:bg-gray-100"} p-2 rounded-full`}
                    >
                        <NavigationSvgIcon title={tab.name} />
                    </button>
                )
            })}
        </div>
    )
}